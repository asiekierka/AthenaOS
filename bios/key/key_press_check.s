	.arch	i186
	.code16
	.intel_syntax noprefix

#include "../common.inc"

/**
 * INT 11h AH=00h - key_press_check
 * Input:
 * Output:
 * - AX = held keys
 */
    .global key_press_check
key_press_check:
    ss mov ax, [keys_held]
    .global __key_clear_pressed
__key_clear_pressed:
    ss mov word ptr [keys_pressed], 0
    ss mov word ptr [keys_pressed_repeat], 0
    ret
